static void dump_hex(const char* label, const uint8_t* bytes, size_t n) {
    printf("  %-12s ", label);
    for (size_t i = 0; i < n; i++) printf("%02x", bytes[i]);
    printf("\n");
}

static int run_vector(const tv_t* tv) {
    printf("[%s]\n", tv->name);
    uint8_t pub[32], prv[64], sig[64];

    ed25519_create_keypair(pub, prv, tv->seed);
    if (memcmp(pub, tv->pub_expected, 32) != 0) {
        printf("  KEYPAIR FAIL\n");
        dump_hex("got", pub, 32);
        dump_hex("expected", tv->pub_expected, 32);
        return 1;
    }
    printf("  keypair: PASS\n");

    ed25519_sign(sig, tv->msg, tv->msg_len, pub, prv);
    if (memcmp(sig, tv->sig_expected, 64) != 0) {
        printf("  SIGN FAIL\n");
        dump_hex("got", sig, 64);
        dump_hex("expected", tv->sig_expected, 64);
        return 1;
    }
    printf("  sign:    PASS\n");
    return 0;
}

int main(void) {
    int          failures = 0;
    const size_t n        = sizeof(TVS) / sizeof(TVS[0]);
    printf("Ed25519 host-side RFC 8032 self-test (%zu vectors)\n\n", n);
    for (size_t i = 0; i < n; i++) {
        failures += run_vector(&TVS[i]);
    }
    printf("\n");
    if (failures == 0) {
        printf("ALL PASS — ed25519_mpi shipping with this build matches RFC 8032\n");
        return 0;
    }
    printf("FAIL (%d vector(s) mismatched) — build cannot ship\n", failures);
    return 1;
}
